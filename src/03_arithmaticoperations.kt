fun main() {

    val num1 = 10.0
    val num2 = 3.0
    var op: Double

    op = num1 + num2
    println("Addition: $op")

    op = num1 - num2
    println("Subtraction: $op")

    op = num1 * num2
    println("Multiplication: $op")

    op = num1 / num2
    println("Division: $op")

    op = num1 % num2
    println("Modulus: $op")
}
